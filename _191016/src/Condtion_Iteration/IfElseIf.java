package Condtion_Iteration;

public class IfElseIf {
	public static void main(String[] args) {
		int score = 90; // S A B C
		if (score >= 95)
			System.out.println("S");
		else if (score >= 90)
			System.out.println("A");
		else if (score >= 80)
			System.out.println("B");
		else
			System.out.println("C");
		
		
		
	}

}
